#ifndef openmf_basic_type_h__
#define openmf_basic_type_h__
#pragma once

//------------------------------------------------------------------------------------------------------//
// Basic typedef for VC++
//------------------------------------------------------------------------------------------------------//

typedef signed char			s8_t;								// signed 8bit
typedef unsigned char		u8_t;								// unsigned 8bit
typedef signed short		s16_t;								// signed 16bit
typedef unsigned short		u16_t;								// unsigned 16bit
typedef signed long			s32_t;								// signed 32bit
typedef unsigned long		u32_t;								// unsigned 32bit
typedef float				f32_t;								// floating point 32bit
typedef double				f64_t;								// floating point 64bit

//------------------------------------------------------------------------------------------------------//

#endif															// openmf_basic_type_h__
